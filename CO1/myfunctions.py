def sum(a,b):
    "Adding two numbers a and b"
    sum=a+b
    return sum
def diff(a,b):
    "Difference of two numbers a and b"
    diff=a-b
    return diff
def mul(a,b):
    "Multiplication of two numbers a and b"
    mul=a*b
    return mul
def div(a,b):
    "Division of two numbers a and b"
    div=a/b
    return div
